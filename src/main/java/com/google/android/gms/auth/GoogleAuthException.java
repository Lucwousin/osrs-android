package com.google.android.gms.auth;

public class GoogleAuthException extends Exception {
    public GoogleAuthException() {
        super();
    }

    public GoogleAuthException(String arg1) {
        super(arg1);
    }

    public GoogleAuthException(String arg1, Throwable arg2) {
        super(arg1, arg2);
    }

    public GoogleAuthException(Throwable arg1) {
        super(arg1);
    }
}

